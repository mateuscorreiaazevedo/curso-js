// OPERADORES DE ATRIBUIÇÃO
// Operador básico "="
let value = 1
console.log(`valor inicial: ${value}`)
// += soma o valor inicial atribuido com a nova atribuição
value += 2
console.log(`valor somado: ${value}`)

// -= subtrai o valor inicial atribuido pela nova atribuição
value -= 1
console.log(`valor subtraído: ${value}`)
/* *= multiplica o valor inicial atribuido pela nova atribuição */
value *= 10
console.log(`valor multiplicado: ${value}`)
//  /= divide o valor inicial atribuido pela nova atribuição
value /= 5
console.log(`valor dividido: ${value}`)
