// Tipos primitivos são dados que não são representados através de um Objeto e, por consequência, não possui métodos.
// Podem ser:
// string
// number
// boolean
// null
// undefined


// String
let nome = "Mateus Azevedo"
console.log(typeof nome,": ", nome)
// Number
let idade = 22
console.log(typeof idade,": ", idade)
// Boolean
let boolean = true
console.log(typeof boolean,": ", boolean)
// Null
let nulo = null
console.log(typeof nulo, ": ", nulo)
// Undefined
let under
console.log(typeof under, ": ", under)