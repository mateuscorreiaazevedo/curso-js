// Operadores de COMPARAÇÃO

let idade = 22
// Comparação boolean (true or false)
// Maior que
console.log(idade > 30)
// Menor que
console.log(`É menor que: ${idade < 30}`)
// Igual
console.log (idade == 22)
// igual em string (vai ser verdadeiro)
console.log(`Igual em string: ${idade == "22"}`)
// igualdade restrita em string (vai ser falso)
console.log(`Igual em string: ${idade === "22"}`)
// Diferente
console.log(idade != 22)
